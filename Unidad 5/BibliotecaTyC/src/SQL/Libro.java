    private void jButVerLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButVerLibrosActionPerformed
        // TODO add your handling code here:
        String texto = "";
        try {
            c.ConectarBasedeDatos("");
            String sql = "use Biblioteca "
            + "Select * from Libro";
            c.resultado = c.sentencia.executeQuery(sql);
                  while(c.resultado.next()){
                texto += "ID Libro = " + c.resultado.getInt("IDLibro")
                +", Codigo = "+c.resultado.getInt("Codigo")
                +", Titulo = "+c.resultado.getString("Titulo")
                +", ISBN = "+c.resultado.getString("ISBN")
                +", Editorial = "+c.resultado.getString("Editorial")
                +", Numero de pagina = "+c.resultado.getInt("NumPag")
                +", ID Autor = "+c.resultado.getInt("IDAutor")+"\n";
              }
        }catch(SQLException ex){
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextLibro.setText(texto);

    }//GEN-LAST:event_jButVerLibrosActionPerformed

    private void jButAggLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAggLibrosActionPerformed
        // TODO add your handling code here:
        try{
            c.ConectarBasedeDatos("");
            String sql = "use Biblioteca "
            + "insert into Libro values("
            + jTextIDLibro.getText()+","
            + jTextCodigo.getText()+","
            +"'"+jTextTitulo.getText()+"',"
            +"'"+jTextISBN.getText()+"',"
            +"'"+jTextEditorial.getText()+"',"
            +jTextNumPag.getText()+","
            +jTextIDAutor.getText()+")";

            c.sentencia.execute(sql);
            JOptionPane.showMessageDialog(this, "El Libro se agregó correctamente");
            c.DesconectarBasedeDatos();
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Error en: "+ex.getMessage());
            try {
                JOptionPane.showMessageDialog(this, "Se aplicara un RollBack");
                c.conexion.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(Autores.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
        jButVerLibros.doClick();
    }//GEN-LAST:event_jButAggLibrosActionPerformed

