    private void jButEncriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButEncriptarActionPerformed
        // TODO add your handling code here:
        try{
            c.ConectarBasedeDatos("");
            String sql = "use Biblioteca "
            +"OPEN SYMMETRIC KEY MySymmetricKey "
            +"DECRYPTION BY CERTIFICATE MyCertificate; "
            +"Update Usuario "
            +"Set IDUsuario_Encrypted = EncryptByKey(Key_GUID('MySymmetricKey'),CONVERT(varbinary, IDUsuario));";

            c.sentencia.execute(sql);
            JOptionPane.showMessageDialog(this, "El ID del Usuario se a encriptado correctamente");
            c.DesconectarBasedeDatos();
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButDesencriptar.setEnabled(true);
        jButEncriptar.setEnabled(false);
        jRadioEncriptado.setSelected(true);
        jButVerUsuarios.doClick();
    }//GEN-LAST:event_jButEncriptarActionPerformed

    private void jButDesencriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButDesencriptarActionPerformed
        // TODO add your handling code here:
        String texto = "";
        try{
            c.ConectarBasedeDatos("");
            String sql = "use Biblioteca "
            +"Select IDUsuario, IDUsuario_Encrypted AS 'ID Usuario Encriptado',"
            +"CONVERT(int ,DECRYPTBYKEY(IDUsuario_Encrypted,0, "
            +"HashBytes('SHA1',CONVERT(varbinary,IDUsuario)))) "
            +"AS 'Decrypted ID Usuario' from Usuario";

            c.resultado = c.sentencia.executeQuery(sql);

            JOptionPane.showMessageDialog(this, "El ID del Libro se a desencriptado correctamente");
            c.DesconectarBasedeDatos();
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButDesencriptar.setEnabled(false);
        jButEncriptar.setEnabled(true);
        jRadioDesencriptado.setSelected(true);
        jButVerUsuarios.doClick();
    }//GEN-LAST:event_jButDesencriptarActionPerformed

