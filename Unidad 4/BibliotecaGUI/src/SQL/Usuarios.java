    private void jButVerUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButVerUsuariosActionPerformed
        // TODO add your handling code here:
        String texto = "";
        try {
        c.ConectarBasedeDatos("");
        String sql = "use Biblioteca "
                + "Select * from Usuario";
        c.resultado = c.sentencia.executeQuery(sql);
        if(jRadioEncriptado.isSelected()){
        while(c.resultado.next()){
        texto +="ID Usuario= " + c.resultado.getString("IDUsuario_Encrypted")
                +", Numero Usuario = "+c.resultado.getInt("NumUsuario")
                +", DNI = "+c.resultado.getString("NIF")
                +", Nombre = "+c.resultado.getString("Nombre")
                +", Dirección = "+c.resultado.getString("Direccion")
                +", Telefono = "+c.resultado.getString("Telefono")
                +"\n";
        }
        }else 
        while(c.resultado.next()){
        texto += "ID Usuario= " + c.resultado.getInt("IDUsuario")
                +", Numero Usuario = "+c.resultado.getInt("NumUsuario")
                +", DNI = "+c.resultado.getString("NIF")
                +", Nombre = "+c.resultado.getString("Nombre")
                +", Dirección = "+c.resultado.getString("Direccion")
                +", Telefono = "+c.resultado.getString("Telefono")+"\n";
        }

        }catch(SQLException ex){
        Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextUsuario.setText(texto);
    }//GEN-LAST:event_jButVerUsuariosActionPerformed

